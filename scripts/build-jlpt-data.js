const fs = require('fs/promises');
const https = require('https');
const path = require('path');

const SOURCE_URL = 'https://raw.githubusercontent.com/Bluskyo/JLPT_Vocabulary/main/data/vocab/results/JLPT_vocab_ALL.json';

const OUTPUT_FILE = path.resolve(
    __dirname,
    '../src/modules/jlpt/data/jlptVocab.js'
);

const SOURCE_NOTICE = `Generated by scripts/build-jlpt-data.js.

Source data:
- Original JLPT data: Tanos / Jonathan Waller
- Converted JSON source: Bluskyo/JLPT_Vocabulary
- Original Tanos data is licensed under Creative Commons BY.
- Bluskyo conversion project is MIT licensed.

This file is generated. Do not edit by hand.
`;

async function main() {
    console.log('Downloading JLPT vocabulary data...');
    const sourceData = await downloadJson(SOURCE_URL);

    console.log('Converting JLPT vocabulary data...');
    const convertedData = convertJlptData(sourceData);

    await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });

    const output = [
        '/*',
        ...SOURCE_NOTICE.trim().split('\n').map(line => ` * ${line}`),
        ' */',
        '',
        `export const JLPT_VOCAB = Object.freeze(${JSON.stringify(convertedData)});`,
        '',
        `export const JLPT_VOCAB_SOURCE = Object.freeze(${JSON.stringify({
            original: 'Tanos / Jonathan Waller',
            conversion: 'Bluskyo/JLPT_Vocabulary',
            license: 'Creative Commons BY for original Tanos data; MIT for conversion project',
            generatedAt: new Date().toISOString()
        }, null, 4)});`,
        ''
    ].join('\n');

    await fs.writeFile(OUTPUT_FILE, output, 'utf8');

    const keyCount = Object.keys(convertedData).length;
    const entryCount = Object.values(convertedData).reduce(
        (total, entries) => total + entries.length,
        0
    );

    console.log(`Wrote ${OUTPUT_FILE}`);
    console.log(`Created ${keyCount} lookup keys with ${entryCount} entries.`);
}

function convertJlptData(sourceData) {
    const output = {};

    Object.entries(sourceData || {}).forEach(([expression, entries]) => {
        const cleanExpression = normaliseExpression(expression);

        if (!cleanExpression || !Array.isArray(entries)) {
            return;
        }

        entries.forEach(entry => {
            const reading = normaliseReading(entry?.reading || '');
            const level = normaliseLevel(entry?.level);

            if (!level) {
                return;
            }

            addEntry(output, cleanExpression, reading, level);

            if (reading) {
                addEntry(output, `${cleanExpression}|${reading}`, reading, level);
            }
        });
    });

    return sortObject(output);
}

function addEntry(output, key, reading, level) {
    const cleanKey = normaliseExpression(key);

    if (!cleanKey || !level) {
        return;
    }

    if (!output[cleanKey]) {
        output[cleanKey] = [];
    }

    const candidate = {
        r: reading || '',
        l: level
    };

    const duplicate = output[cleanKey].some(existing =>
        existing.r === candidate.r && existing.l === candidate.l
    );

    if (!duplicate) {
        output[cleanKey].push(candidate);
    }

    output[cleanKey].sort((a, b) => {
        if (a.r !== b.r) {
            return a.r.localeCompare(b.r, 'ja');
        }

        return getLevelRank(a.l) - getLevelRank(b.l);
    });
}

function sortObject(object) {
    return Object.fromEntries(
        Object.entries(object).sort(([a], [b]) => a.localeCompare(b, 'ja'))
    );
}

function normaliseExpression(value) {
    return String(value || '')
        .trim()
        .replace(/\s+/g, '')
        .replace(/^=/, '')
        .replace(/[［］\[\]]/g, '')
        .replace(/[（）()]/g, '');
}

function normaliseReading(value) {
    return katakanaToHiragana(
        String(value || '')
            .trim()
            .replace(/\s+/g, '')
            .replace(/[（）()]/g, '')
            .replace(/[［］\[\]]/g, '')
    );
}

function normaliseLevel(value) {
    const text = String(value || '').toLowerCase();
    const match = text.match(/[1-5]/);

    if (!match) {
        return '';
    }

    return `n${match[0]}`;
}

function getLevelRank(level) {
    const ranks = {
        n5: 1,
        n4: 2,
        n3: 3,
        n2: 4,
        n1: 5
    };

    return ranks[level] || 999;
}

function katakanaToHiragana(value) {
    return String(value || '').replace(/[\u30A1-\u30F6]/g, char =>
        String.fromCharCode(char.charCodeAt(0) - 0x60)
    );
}

function downloadJson(url) {
    return new Promise((resolve, reject) => {
        downloadText(url)
            .then(text => {
                try {
                    resolve(JSON.parse(text));
                } catch (error) {
                    reject(new Error(`Failed to parse JSON from ${url}: ${error.message}`));
                }
            })
            .catch(reject);
    });
}

function downloadText(url, redirectCount = 0) {
    return new Promise((resolve, reject) => {
        if (redirectCount > 5) {
            reject(new Error('Too many redirects while downloading JLPT data.'));
            return;
        }

        const request = https.get(
            url,
            {
                headers: {
                    'User-Agent': 'YomiSaver-JLPT-Data-Builder'
                }
            },
            response => {
                const statusCode = response.statusCode || 0;

                if ([301, 302, 303, 307, 308].includes(statusCode)) {
                    const location = response.headers.location;

                    if (!location) {
                        reject(new Error(`Redirect from ${url} had no location header.`));
                        return;
                    }

                    const nextUrl = new URL(location, url).toString();
                    resolve(downloadText(nextUrl, redirectCount + 1));
                    return;
                }

                if (statusCode < 200 || statusCode >= 300) {
                    reject(new Error(`Download failed with status ${statusCode}: ${url}`));
                    return;
                }

                let body = '';

                response.setEncoding('utf8');

                response.on('data', chunk => {
                    body += chunk;
                });

                response.on('end', () => {
                    resolve(body);
                });
            }
        );

        request.on('error', reject);
        request.setTimeout(30000, () => {
            request.destroy(new Error('Download timed out while fetching JLPT data.'));
        });
    });
}

main().catch(error => {
    console.error(error);
    process.exitCode = 1;
});